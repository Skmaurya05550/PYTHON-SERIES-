# Class and object
# Explanation : OOP is a programming paradigm based on the concept of objects .


class Dog:
    def __init__(self, name):
        self.name = name

    def bark(self):
        return "Woof!"

my_dog = Dog("Buddy")
print(my_dog.bark())

